<script setup>
import Snowflakes from "magic-snowflakes";
import { PaperAirplaneIcon } from "@heroicons/vue/24/outline";
import { marked } from "marked";

const page = ref("intro");
const chatContainer = ref(null);
const loading = ref(false);
const error = ref(false);
const message = ref(
  "My dad loves the ocean and do some hikes. He is 56 years old. He used to go a lot to the Alps and the Himilaya. But now is sitting a lot at home. I would love to get him moving again."
);
const messages = ref([]);

messages.value.push({
  role: "assistant",
  content:
    "Hi, I'm Santa's assistant. Describe the person you want to give a gift to and I'll help you find the perfect present.",
});

async function sendMessage() {
  if (!message.value) return;
  loading.value = true;
  error.value = false;
  messages.value.push({
    role: "user",
    content: message.value,
  });
  message.value = "";
  scrollToBottom();

  try {
    let res = await $fetch(`/api/v1/chat`, {
      method: "POST",
      body: JSON.stringify({
        messages: messages.value,
      }),
    });
    message.value = "";

    let newMessage = ref({
      role: "assistant",
      content: res.explanation,
      products: res.products,
    });
    messages.value.push(newMessage.value);
  } catch (err) {
    error.value = true;
  }

  loading.value = false;
  scrollToBottom();
  message.value = "";
}

onMounted(() => {
  const snowflakes = new Snowflakes({
    container: document.querySelector("#container"),
    color: "white",
    zIndex: -1,
  });
});

function scrollToBottom() {
  nextTick(() => {
    if (chatContainer.value) {
      chatContainer.value.scrollTop = chatContainer.value.scrollHeight;
    }
  });
}
</script>

